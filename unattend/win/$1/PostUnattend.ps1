switch ( $runFunction )
{
    RegMachine {
        Write-MSG "Setting registry local machine"
        & $runFunction
        if ( $verboseLog ) {
            Stop-Transcript
        }
    }
    RegAllUser {
        Write-MSG "Setting registry for user"
        $regRoot = "HKLM:\AllProfile"
        LoadDefaultUserHive; RegUser; UnloadDefaultUserHive
        if ( $verboseLog ) {
            Stop-Transcript
        }
    }
    RemoveApps {
        Write-MSG "Removing unwanted applications"
        & $runFunction
        if ( $verboseLog ) {
            Stop-Transcript
        }
    }
    InstallApps {
        Write-MSG "Installing applications"
        & $runFunction
        if ( $verboseLog ) {
            Stop-Transcript
        }
    }
    NewStartLayout {
        Write-MSG "Setting clean start menu for profiles"
        & $runFunction
        if ( $verboseLog ) {
            Stop-Transcript
        }
    }
    CleanUp {
        Write-MSG "Cleaning up"
        & $runFunction
        if ( $verboseLog ) {
            Stop-Transcript
        }
    }
    AdminSettings {
        Write-MSG "Admin account setup"
        & $runFunction
        if ( $verboseLog ) {
            Stop-Transcript
        }
        Restart-Computer -Force
    }
    MySettings {
        if ( $env:USERNAME -eq $myName ) {
            Write-MSG "My application settings"
            & $runFunction
        }
        if ( $verboseLog ) {
            Stop-Transcript
            $paramsSendMailmessage = @{
                SmtpServer  = "$smtpServer"
                From        = "$fromEmail"
                To          = "$myEmail"
                Subject     = "$($script:systemName.Vendor) $($script:systemName.Version) - $($script:systemBios.SerialNumber)"
