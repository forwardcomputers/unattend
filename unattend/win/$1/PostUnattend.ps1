#region code Remove unwanted applications
function RemoveApps {
    Write-MSG "Apps to keep"
    $appsToKeep = "calculator|store|weather|MicrosoftEdgeDevToolsClient"

    Get-AppxPackage -AllUsers |
        Where-Object {$_.Name -NotMatch $appsToKeep} |
        Remove-AppxPackage -ErrorAction SilentlyContinue
    Get-AppXProvisionedPackage -Online |
        Where-Object {$_.DisplayName -NotMatch $appsToKeep} |
        Remove-AppxProvisionedPackage -Online -ErrorAction SilentlyContinue

    $appLists = @(
        "ContactSupport"
        "Media.WindowsMediaPlayer"
        )
    foreach ($app in $appLists) {
        Remove-WindowsCapability -Name "*$app*" -Online -ErrorAction SilentlyContinue
    }
    Write-MSG "Remove Microsoft XPS Document Writer"
    Disable-WindowsOptionalFeature -NoRestart -Online -FeatureName "Printing-XPSServices-Features"
    Write-MSG "Enable Hyper-V"
    Enable-WindowsOptionalFeature -NoRestart -Online -All -FeatureName "Microsoft-Hyper-V"
}
