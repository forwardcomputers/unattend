    $paramsSetItemProperty = @{
        Path    = "HKLM:\Software\Microsoft\Windows\CurrentVersion\RunOnce"
        Name    = "Run1"
        Type    = "ExpandString"
        Value   = '%SystemRoot%\System32\WindowsPowerShell\v1.0\powershell.exe -ExecutionPolicy ByPass -NoProfile -Command "\\filer\share\os\data\unattend\win\$1\PostUnattend.ps1 AdminSettings"'
    }
    Set-ItemProperty @paramsSetItemProperty

    Write-MSG "Clean up"
    $dirList = @(
        "$env:LOCALAPPDATA\Nuget"
        "$env:LOCALAPPDATA\temp"
        "$env:ProgramData\Microsoft\Windows\WER\ReportArchive\*"
        "$env:SystemDrive\Intel"
        "$env:SystemDrive\PerfLogs"
        "$env:SystemRoot\logs"
        "$env:SystemRoot\temp"
        "$env:SystemRoot\Panther"
        "$env:SystemRoot\SoftwareDistribution\Download\*"
        "$env:SystemRoot\System32\Sysprep\Panther"
        "$env:SystemRoot\winsxs\manifestcache"
    )
    foreach ($dir in $dirList) {
