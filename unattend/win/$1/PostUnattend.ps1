# General settings
function GeneralSettings {
    $script:osName      = (Get-CimInstance -ClassName Win32_OperatingSystem).Caption
    $script:osVersion 	= (Get-Item -Path 'HKLM:\SOFTWARE\Microsoft\Windows NT\CurrentVersion').GetValue('ReleaseID')
    $script:systemName  = Get-CimInstance -ClassName Win32_ComputerSystemProduct | Select-Object "Vendor","Version"
    $script:systemBios  = Get-CimInstance -ClassName Win32_BIOS | Select-Object "Manufacturer","SerialNumber"
    
    if ( $script:systemBios.Manufacturer -like "*Microsoft*" ) {
        $script:isVM = "hyperv"
    }
    elseif ( $script:systemBios.Manufacturer -like "*innotek*" ) {
        $script:isVM = "virtualbox"
    }
    elseif ( $script:systemBios.Manufacturer -like "*Parallels*" ) {
        $script:isVM = "parallels"
    }
    elseif ( $script:systemBios.Manufacturer -like "*VMware*" ) {
        $script:isVM = "vmware"
    }
    else {
        $script:isVM = $false
    }
}
