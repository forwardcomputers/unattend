function Invoke-NativeExpression {
    Param (
        [Parameter(Mandatory=$true,ValueFromPipeline=$true,Position=0)]
        [ValidateNotNullOrEmpty()]
        [String]$doExpression
    )
 
    process {
        $doExecutable,$doArguments = $doExpression -split ' '
        $doArguments = $doArguments | foreach {"$_"}
        $doArguments = $doArguments -join ' '
        $doCommand = $doExecutable + ' ' + $doArguments
         if ( $doCommand ) {
            Write-MSG "$doCommand"
            Invoke-Expression -command $doCommand
        }
    }
}

# Kill process after it starts
function KillIt {
    [CmdletBinding()]
    Param (
        [Parameter(Mandatory = $true, Position = 0)]
        [ValidateNotNullOrEmpty()]
        [String]$processName
    )

    $processStarted = $false
    Do {
        $processStatus = Get-Process -Name $processName -ErrorAction SilentlyContinue
        if ( !($processStatus) ) {
            Start-Sleep -Seconds 20
        } else {
            Stop-Process -Name $processName ; $processStarted = $true
        }
    }
    Until ( $processStarted )
}

# Load default user hive
function LoadDefaultUserHive {
    $defaultuserlocation = (Get-ItemProperty -Path "HKLM:\Software\Microsoft\Windows NT\CurrentVersion\ProfileList").Default + "\NTUSER.DAT"
    $hive = $regRoot -replace '[:]',''
