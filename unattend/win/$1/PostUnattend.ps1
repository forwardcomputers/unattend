    powercfg.exe /change hibernate-timeout-ac 0 | Out-Default
    powercfg.exe /change standby-timeout-ac 0 | Out-Default
    if ( $script:isVM ) {
        powercfg.exe /change monitor-timeout-ac 0 | Out-Default
