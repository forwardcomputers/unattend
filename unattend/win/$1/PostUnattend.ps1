    [System.GC]::collect()
    $hive = $regRoot -replace '[:]',''
    reg.exe unload $hive | Out-Default
}

# Download latest cumulative update
function DownloadWindowsUpdate {
    # https://support.microsoft.com/app/content/api/content/asset/en-us/4000816
    # Windows 10 version 1511 - 4000824
    # Windows 10 version 1607 - 4000825
    # Windows 10 version 1703 - 4018124
    # Windows 10 version 1709 - 4043454
    # Windows 10 version 1803 - 4099479
    # Windows 10 version 1809 - 4464619

    $wuOSBuild = [System.Environment]::OSVersion.Version.Build
    
    $wuarticleID = ( ( Invoke-RestMethod -UseBasicParsing https://support.microsoft.com/app/content/api/content/asset/en-us/4000816 ).links | 
        Where-Object { $_.text -like "Windows 10 Version $script:osVersion" } ).articleID
    
    $wukbID = ( ( Invoke-WebRequest -UseBasicParsing "https://support.microsoft.com/en-us/help/$wuarticleID" ).content | 
        Select-String '"([^\-\"]*)—(KB[0-9]*) \(OS Build ([0-9\.]*)\)"' -AllMatches ).Matches[0].Groups[2].value
    
    $wukbPage = Invoke-WebRequest -UseBasicParsing https://www.catalog.update.microsoft.com/Search.aspx?q=Cumulative%20Windows%2010%20x64%20$wukbID
    
    $wukbIDs = $wukbPage.InputFields |
        Where-Object { $_.Type -eq 'Button' -and $_.Value -eq 'Download' } |
        Select-Object -ExpandProperty ID |
        Select -First 1
    
    $wuURL = @()
    $wupost = @{ size = 0; updateID = $wukbIDs; uidInfo = $wukbIDs } | ConvertTo-Json -Compress
    $wupostBody = @{ updateIDs = "[$wupost]" } 
    $wuURL += Invoke-WebRequest -UseBasicParsing -Uri 'http://www.catalog.update.microsoft.com/DownloadDialog.aspx' -Method Post -Body $wupostBody |
        Select-Object -ExpandProperty Content |
        Select-String -AllMatches -Pattern "(http[s]?\://download\.windowsupdate\.com\/[^\'\""]*)" | 
        %{ $_.Matches.Value }
    
    $wuFile = Split-Path $wuURL -leaf
    
    if ( $wuURL ) {
        Write-MSG "Downloading $wuFile"
        $script:wuDLJob = Start-BitsTransfer -Asynchronous -DisplayName WU -Source $wuURL -Destination $env:Public\Downloads\WindowsUpdate.msu
    }
}
